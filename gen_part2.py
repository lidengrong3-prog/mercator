def py_to_js_val(v, indent=0):
    """Convert Python value to JS literal."""
    if v is None:
        return 'null'
    if isinstance(v, bool):
        return 'true' if v else 'false'
    if isinstance(v, (int, float)):
        return str(v)
    if isinstance(v, str):
        # Escape single quotes and backslashes
        s = v.replace('\\', '\\\\').replace("'", "\\'").replace('\n', '\\n')
        return f"'{s}'"
    if isinstance(v, list):
        if len(v) == 0:
            return '[]'
        # Check if all items are simple (strings/numbers)
        if all(isinstance(x, (str, int, float)) for x in v):
            items = [py_to_js_val(x) for x in v]
            return '[' + ','.join(items) + ']'
        items = [py_to_js_val(x, indent+2) for x in v]
        return '[' + ','.join(items) + ']'
    if isinstance(v, dict):
        if len(v) == 0:
            return '{}'
        parts = []
        for k, val in v.items():
            parts.append(f'{k}:{py_to_js_val(val, indent+2)}')
        return '{' + ','.join(parts) + '}'
    return str(v)

